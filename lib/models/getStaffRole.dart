import 'dart:convert';

GetStaffRole getStaffRoleFromJson(String str) => GetStaffRole.fromJson(json.decode(str));

String getStaffRoleToJson(GetStaffRole data) => json.encode(data.toJson());

class GetStaffRole {
  GetStaffRole({
    this.count,
    this.next,
    this.previous,
    this.results,
  });

  int count;
  dynamic next;
  dynamic previous;
  List<Result> results;

  factory GetStaffRole.fromJson(Map<String, dynamic> json) => GetStaffRole(
    count: json["count"],
    next: json["next"],
    previous: json["previous"],
    results: List<Result>.from(json["results"].map((x) => Result.fromJson(x))),
  );

  Map<String, dynamic> toJson() => {
    "count": count,
    "next": next,
    "previous": previous,
    "results": List<dynamic>.from(results.map((x) => x.toJson())),
  };
}

class Result {
  Result({
    this.id,
    this.name,
  });

  int id;
  String name;

  factory Result.fromJson(Map<String, dynamic> json) => Result(
    id: json["id"],
    name: json["name"],
  );

  Map<String, dynamic> toJson() => {
    "id": id,
    "name": name,
  };
}
